CREATE TABLE IF NOT EXISTS reservations(
  reservation_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  member_id int NOT NULL,
  book_id int NOT NULL,
  reservation_date date NOT NULL DEFAULT CURRENT_DATE,
  reservation_status_id int NOT NULL
);

CREATE TABLE IF NOT EXISTS reservation_status (
  reservation_status_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(20) NOT NULL UNIQUE 
    CONSTRAINT chk_reservation_status_name
    CHECK (trim(name) <> '')
);

CREATE TABLE IF NOT EXISTS borrowings (
  borrowing_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  copy_id int NOT NULL,
  member_id int NOT NULL,
  borrow_date date NOT NULL DEFAULT CURRENT_DATE,
  due_date date NOT NULL
    CONSTRAINT chk_borrowings_due_date
    CHECK ( due_date >= borrow_date ),
  return_date date
    CONSTRAINT chk_borrowings_return_date
    CHECK ( return_date IS NULL OR return_date >= borrow_date ),
  borrowing_status_id int NOT NULL
);

CREATE TABLE IF NOT EXISTS borrowing_status (
  borrowing_status_id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(20) NOT NULL UNIQUE
    CONSTRAINT chk_borrowing_status_name
    CHECK (trim(name) <> '')
);

