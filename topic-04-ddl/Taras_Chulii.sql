CREATE TABLE IF NOT EXISTS members (
  member_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  first_name varchar(50) NOT NULL
    CONSTRAINT chk_members_first_name
    CHECK (trim(first_name) <> ''),
  last_name varchar(50) NOT NULL
    CONSTRAINT chk_members_last_name
    CHECK (trim(last_name) <> ''),
  phone varchar(20) UNIQUE NOT NULL
    CONSTRAINT chk_members_phone
    CHECK ( phone ~ '^\+\d{1,3} \(\d{2,3}\) \d{3}-\d{2}-\d{2}$'),
  address varchar(128) NOT NULL
    CONSTRAINT chk_members_address
    CHECK (trim(address) <> ''),
  email varchar(100) UNIQUE NOT NULL
    CONSTRAINT chk_members_email
    CHECK (email ~* '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$'),
  created_date TIMESTAMP NOT NULL DEFAULT (CURRENT_TIMESTAMP),
  member_status_id smallint NOT NULL
);

CREATE TABLE IF NOT EXISTS member_status (
  member_status_id smallint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(20) NOT NULL UNIQUE
    CONSTRAINT chk_member_status_name
    CHECK (trim(name) <> '')
);

CREATE TABLE IF NOT EXISTS reviews (
  review_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  member_id int NOT NULL,
  book_id int NOT NULL,
  rating smallint NOT NULL
    CONSTRAINT chk_reviews_rating
    CHECK (rating BETWEEN 1 AND 5),
  review_text TEXT,
  created_date TIMESTAMP NOT NULL DEFAULT (CURRENT_TIMESTAMP),
  UNIQUE (member_id, book_id)
);

